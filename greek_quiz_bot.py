# Добавляем букву к каждому слову
WORDS = []
for greek, russian in RAW_WORDS:
    letter = greek[0].upper() if greek else "?"
    WORDS.append({"greek": greek, "russian": russian, "letter": letter})

# Уникальные буквы для фильтрации
LETTERS = sorted(set(w["letter"] for w in WORDS))

# ─────────────────────────── СОСТОЯНИЕ ПОЛЬЗОВАТЕЛЯ ─────────────────────────

def get_user_data(context):
    """Инициализация данных пользователя."""
    d = context.user_data
    if "score" not in d:
        d["score"] = 0
        d["total"] = 0
        d["wrong_words"] = []   # слова с ошибками
        d["filter_letter"] = None  # None = все буквы
        d["current_question"] = None
        d["mode"] = "random"    # random = оба направления случайно
    return d


def get_filtered_words(letter):
    if letter:
        return [w for w in WORDS if w["letter"] == letter]
    return WORDS


def make_question(d):
    """Генерирует вопрос. Чаще показывает слова с ошибками."""
    words = get_filtered_words(d["filter_letter"])
    if not words:
        words = WORDS

    # С вероятностью 30% повторяем слово с ошибкой, если оно есть
    pool = d["wrong_words"] if d["wrong_words"] and random.random() < 0.3 else words

    word = random.choice(pool)

    # Случайное направление
    gr_to_ru = random.choice([True, False])

    if gr_to_ru:
        question = f"🇬🇷 *{word['greek']}*\n\nКак переводится?"
        correct = word["russian"]
        # Берём 3 неправильных ответа из всего словаря
        others = [w["russian"] for w in WORDS if w["russian"] != correct]
    else:
        question = f"🇷🇺 *{word['russian']}*\n\nКак будет по-гречески?"
        correct = word["greek"]
        others = [w["greek"] for w in WORDS if w["greek"] != correct]

    wrong = random.sample(others, min(3, len(others)))
    options = wrong + [correct]
    random.shuffle(options)

    d["current_question"] = {
        "word": word,
        "correct": correct,
        "options": options,
        "gr_to_ru": gr_to_ru,
    }
    return question, options


